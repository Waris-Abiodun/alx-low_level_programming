/**
 * add, sub , mul, divide and mod : are funcions to be use in dynamic library
 * @a: integr
 * @b: integer
 * Return : result of the calc
 */

 int add(int a, int b)
 {
     return (a + b);
 }

  int sub(int a, int b)
 {
     return (a - b);
 }

  int mul(int a, int b)
 {
     return (a * b);
 }

  int div(int a, int b)
 {
     return (a / b);
 }

  int mod(int a, int b)
 {
     return (a % b);
 }